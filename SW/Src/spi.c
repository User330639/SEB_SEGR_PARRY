/* AD9833 Write and Clear methods */
int WriteRegister(uint32_t data)
{
    HAL_GPIO_WritePin(NSS_DDS_GPIO_Port, NSS_DDS_Pin, GPIO_PIN_SET);
    uint8_t _data[3] = {*(uint8_t*)((int)&data + 2),
                        *(uint8_t*)((int)&data + 1),
                        *(uint8_t*)((int)&data + 0)};
    HAL_GPIO_WritePin(NSS_DAC_GPIO_Port, NSS_DAC_Pin, GPIO_PIN_RESET);
    if (HAL_OK != HAL_SPI_Transmit(&hspi2, &_data[0], 3, HAL_MAX_DELAY))
        goto err;
    HAL_GPIO_WritePin(NSS_DAC_GPIO_Port, NSS_DAC_Pin, GPIO_PIN_SET);
    return 0;
err:
    return -1;
}

int Clear(void)
{
    HAL_GPIO_WritePin(DAC_CLR_GPIO_Port, DAC_CLR_Pin, GPIO_PIN_RESET);
    HAL_Delay(1);
    HAL_GPIO_WritePin(DAC_CLR_GPIO_Port, DAC_CLR_Pin, GPIO_PIN_SET);
    HAL_Delay(1);
    return 0;
}

